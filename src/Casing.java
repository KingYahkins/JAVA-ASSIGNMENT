public class Casing {
    public static void main(String[] args) {

        // auto casing
        int y  = 10;

        double a=y;

        //display
        System.out.println("value: "+ a);

        //manual
        double r= 10.00;
        int n = (int) r;
        // display
        System.out.println(n);
    }
}
